// Helper to flatten SEED_DATA into Prompt[]
const generateInitialPrompts = (): Prompt[] => {
  const prompts: Prompt[] = [];
  const now = Date.now();
  let counter = 0;

  // Randomly assign tags for demo purposes
  const DEMO_TAGS = ["ChatGPT", "Claude", "文案", "代码", "办公", "Coze", "DeepSeek", "飞书多维表格"];

  Object.entries(SEED_DATA).forEach(([category, subCats]) => {
    Object.entries(subCats).forEach(([subCategory, items]) => {
      items.forEach((item) => {
        counter++;
        
        // Pick 0-2 random tags
        const numTags = Math.floor(Math.random() * 3);
        const tags: string[] = [];
        for(let i=0; i<numTags; i++) {
            const tag = DEMO_TAGS[Math.floor(Math.random() * DEMO_TAGS.length)];
            if (!tags.includes(tag)) tags.push(tag);
        }

        prompts.push({
          id: `seed-${counter}`,
          title: item.t,
          content: item.c,
          category: category,
          subCategory: subCategory,
          tags: tags,
          rating: item.r,
          createdAt: now - (counter * 60000), 
          updatedAt: now - (counter * 60000),
        });
      });
    });
  });

  return prompts;
};

export const INITIAL_PROMPTS = generateInitialPrompts();
